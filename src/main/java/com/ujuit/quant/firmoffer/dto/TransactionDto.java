package com.ujuit.quant.firmoffer.dto;

import java.io.Serializable;
import java.math.BigDecimal;
import java.util.Date;
import java.util.List;

import com.ujuit.quant.firmoffer.model.TTransactionDetail;

/**
 * 交易详情
 * 
 * @author shadow
 * @date 2017年4月24日
 */
public class TransactionDto implements Serializable {

	private static final long serialVersionUID = 1L;

	private List<TTransactionDetail> ttrList;
	/**
	 * 日期
	 */
	private Date theTime;

	/**
	 * 统计
	 */
	private Integer sum;

	/**
	 * 买入
	 */
	private BigDecimal buying;
	/**
	 * 卖出
	 */
	private BigDecimal sale;

	

	public Date getTheTime() {
		return theTime;
	}

	public void setTheTime(Date theTime) {
		this.theTime = theTime;
	}

	public List<TTransactionDetail> getTtrList() {
		return ttrList;
	}

	public void setTtrList(List<TTransactionDetail> ttrList) {
		this.ttrList = ttrList;
	}

	public Integer getSum() {
		return sum;
	}

	public void setSum(Integer sum) {
		this.sum = sum;
	}

	public BigDecimal getBuying() {
		return buying;
	}

	public void setBuying(BigDecimal buying) {
		this.buying = buying;
	}

	public BigDecimal getSale() {
		return sale;
	}

	public void setSale(BigDecimal sale) {
		this.sale = sale;
	}

}
